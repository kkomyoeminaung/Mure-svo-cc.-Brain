    "            \n",
    "            if (i + 1) % GRAD_ACCUM == 0:\n",
    "                self.opt.step()\n",
    "                self.scheduler.step()\n",
    "                self.opt.zero_grad()\n",
    "            \n",
    "                self.step += 1\n",
    "                # FREQUENT CHECKPOINT: Every 200 optimization steps\n",
