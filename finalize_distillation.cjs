    "                    self.save()\n",
    "            \n",
    "            if i % 10 == 0:\n",
    "                loss_val = loss.item() * GRAD_ACCUM # Real Loss\n",
    "                pbar.set_description(f\"Loss: {loss_val:.4f}\")\n",
    "            \n",
    "            pbar.update(1)\n",
    "        \n",
    "        self.save() # Save at end of epoch\n",
