    "        self.student.train()\n",
    "        GRAD_ACCUM = 4\n",
    "        current_batch_in_epoch = self.step % len(loader)\n",
    "        \n",
    "        print(f\"🚀 Training stats: Total Batches={len(loader)}, Current Step={self.step}\")\n",
    "        pbar = tqdm(total=len(loader), initial=current_batch_in_epoch)\n",
    "        \n",
    "        for i, (input_ids, mask) in enumerate(loader):\n",
    "            # Sequential Resume Skip logic\n",
    "            if self.step > 0 and i < current_batch_in_epoch:\n",
    "                if i % 100 == 0: pbar.update(100)\n",
    "                continue\n",
    "            \n",
    "            ids, mask = input_ids.to(self.device), mask.to(self.device)\n",
    "            with torch.no_grad(): \n",
