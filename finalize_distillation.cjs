    "    def __len__(self): return len(self.data)\n",
    "\n",
    "    def __getitem__(self, i):\n",
    "        rule = self.data[i]\n",
    "        # Construct the distillation prompt\n",
    "        text = f\"Cause: {rule['cause']} -> Effect: {rule['effect']}\"\n",
    "        enc = self.tk(text, truncation=True, padding='max_length', max_length=128, return_tensors='pt')\n",
    "        return enc['input_ids'].squeeze(), enc['attention_mask'].squeeze()\n",
    "\n",
    "class DistillationTrainer:\n",
    "    def __init__(self, t, s, base_path):\n",
    "        self.device = 'cuda' if torch.cuda.is_available() else 'cpu'\n",
    "        self.teacher = t\n",
    "        self.student = s\n",
    "        self.checkpoint_dir = os.path.join(base_path, \"checkpoints\")\n",
    "        os.makedirs(self.checkpoint_dir, exist_ok=True)\n",
    "        \n",
    "        self.opt = AdamW(self.student.parameters(), lr=4e-5)\n",
    "        self.step = 0\n",
    "        self._load_resume()\n",
    "\n",
    "    def _load_resume(self):\n",
    "        path = os.path.join(self.checkpoint_dir, \"latest_distill.pt\")\n",
    "        if os.path.exists(path):\n",
    "            print(f\"🔍 Found checkpoint: {path}. Resuming...\")\n",
    "            ckpt = torch.load(path, map_location='cpu')\n",
    "            self.student.load_state_dict(ckpt['model'])\n",
    "            self.opt.load_state_dict(ckpt['opt'])\n",
    "            self.step = ckpt.get('step', 0)\n",
    "            print(f\"✅ Resumed distillation from Step {self.step}\")\n",
    "        else:\n",
    "            print(\"🆕 No existing checkpoint found. Starting fresh.\")\n",
    "        \n",
    "        self.student.to(self.device)\n",
    "        # Re-initialize scheduler relative to current step\n",
    "        self.scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(self.opt, T_max=1000000, eta_min=1e-6)\n",
    "        if os.path.exists(path) and 'scheduler' in ckpt:\n",
    "            try:\n",
    "                self.scheduler.load_state_dict(ckpt['scheduler'])\n",
    "            except: pass\n",
    "\n",
    "    def save(self):\n",
    "        path = os.path.join(self.checkpoint_dir, \"latest_distill.pt\")\n",
    "        ckpt = {\n",
    "            'model': self.student.state_dict(), \n",
    "            'opt': self.opt.state_dict(), \n",
    "            'scheduler': self.scheduler.state_dict(),\n",
    "            'step': self.step\n",
    "        }\n",
