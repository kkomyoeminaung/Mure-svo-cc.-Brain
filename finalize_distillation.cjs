    "\n",
    "class RuleDataset(Dataset):\n",
    "    def __init__(self, path, tk):\n",
    "        print(f\"📖 Mapping Rule Dataset from: {path}\")\n",
    "        try:\n",
    "            from datasets import load_dataset\n",
    "            # Efficient lazy-loading using HuggingFace datasets library\n",
    "            self.data = load_dataset('json', data_files=path, split='train')\n",
